public class data_types {
    public static void main(String[] args) {
        // primitive data types in java
        // int
        int number;
        number = 123;
        // char
        char ch = 't';
        // float
        float radius = 4.5f; // you've to add a 'f' at the end
        // double- to store large decimal numbers
        double largeDecimalNum= 93.212345657;
        // long - to store large integer value
        long largeIntegerNum = 434909829301238L;
        // boolean - either true or false
        boolean bool = true;


        // displaying the values of variables
        System.out.println(number);
        System.out.println(ch);
        System.out.println(radius);
        System.out.println(largeDecimalNum);
        System.out.println(largeIntegerNum);
        System.out.println(bool);
    }
}
